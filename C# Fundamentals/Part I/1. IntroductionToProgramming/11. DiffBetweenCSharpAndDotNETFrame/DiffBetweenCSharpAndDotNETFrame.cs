// Describe the difference between C# and .NET Framework.

/*
 * C# is a programming language encompassing strong typing, imperative, declarative,
 functional, procedural, generic, object-oriented (class-based), and 
 component-oriented programming disciplines. It was developed by Microsoft.
 C# is one of the programming languages designed for the Common Language Infrastructure.

 The .NET Framework is a software framework developed by Microsoft that runs 
 primarily on Microsoft Windows. It includes a large library and provides 
 language interoperability (each language can use code written in other languages)
 across several programming languages. Programs written for the .NET Framework
 execute in a software environment, known as the Common Language Runtime (CLR),
 an application virtual machine that provides services such as security, memory
 management, and exception handling. The class library and the CLR together
 constitute the .NET Framework.
*/