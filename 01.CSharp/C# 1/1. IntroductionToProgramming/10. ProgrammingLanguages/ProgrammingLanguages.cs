/*

 Assembly language - a low-level programming language for a computer, or other
 programmable device, in which there is a very strong (generally one-to-one) 
 correspondence between the language and the architecture's machine code 
 instructions. Each assembly language is specific to a particular computer 
 architecture, in contrast to most high-level programming languages, which 
 are generally portable across multiple systems.

C# - a programming language encompassing strong typing, imperative, declarative,
functional, procedural, generic, object-oriented (class-based), and 
component-oriented programming disciplines. It was developed by Microsoft.
C# is one of the programming languages designed for the Common Language 
Infrastructure.

C - a general-purpose programming language initially developed between 1969 
and 1973. Like most imperative languages in the ALGOL tradition, C has 
facilities for structured programming and allows lexical variable scope 
and recursion, while a static type system prevents many unintended operations. 
C program source code is free-format, using the semicolon as a statement 
terminator and curly braces for grouping blocks of statements. C is one 
of the most widely used programming languages of all time, and there are 
very few computer architectures for which a C compiler does not exist.

PHP - a server-side scripting language designed for web development but 
also used as a general-purpose programming language. PHP is now installed 
on more than 244 million websites and 2.1 million web servers. Originally 
created by Rasmus Lerdorf in 1995. PHP code is interpreted by a web server 
with a PHP processor module which generates the resulting web page: PHP 
commands can be embedded directly into an HTML source document rather 
than calling an external file to process data.

Java - a general-purpose, concurrent, class-based, object-oriented 
computer programming language that is specifically designed to have as 
few implementation dependencies as possible. It is intended to let 
application developers "write once, run anywhere" (WORA), meaning that 
code that runs on one platform does not need to be recompiled to run on 
another. Java applications are typically compiled to bytecode that can 
run on any Java virtual machine (JVM).

Pascal - an influential imperative and procedural programming language, 
designed in 1968–1969 and published in 1970 by Niklaus Wirth as a small 
and efficient language intended to encourage good programming practices 
using structured programming and data structuring. A derivative known as 
Object Pascal designed for object-oriented programming was developed in 1985.

*/